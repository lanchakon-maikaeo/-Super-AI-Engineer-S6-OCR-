def process_pipeline(doc_groups, image_map):
    results_map = {}
    total_rows_processed = 0
    last_notified_rows = 0

    checkpoint_log = {}
    if os.path.exists(CONFIG["json_log_path"]):
        try:
            with open(CONFIG["json_log_path"], "r", encoding="utf-8") as f:
                checkpoint_log = json.load(f)
        except json.JSONDecodeError: checkpoint_log = {}

    global_val_list = []
    if os.path.exists(CONFIG["validation_csv"]):
        try: global_val_list = pd.read_csv(CONFIG["validation_csv"]).to_dict('records')
        except pd.errors.EmptyDataError: pass

    for doc_key, rows in doc_groups.items():
        if doc_key in checkpoint_log and "final_results" in checkpoint_log[doc_key]:
            for template_id, vote in checkpoint_log[doc_key]["final_results"].items():
                results_map[template_id] = vote
            total_rows_processed += len(rows)
            continue

        pages = image_map.get(doc_key, [])
        if not pages: continue

        print(f"Processing: {doc_key} (Expect {len(rows)} rows)")

        table_roi = extract_table_grid(pages, doc_key)

        # 🚀 FIX 3: เอามีดสับเฉพาะ 60% ฝั่งขวาของตาราง เพื่อลด Noise ชื่อพรรค และลดภาระ Gemini
        if table_roi is not None:
            h_roi, w_roi = table_roi.shape[:2]
            table_roi = table_roi[:, int(w_roi * 0.40):]

        crop_path = os.path.join(CONFIG["crop_dir"], f"crop_{doc_key}.png")
        if table_roi is not None: cv2.imwrite(crop_path, table_roi)

        extracted_data, conf, is_valid, raw_ocr_blocks, val_rows = run_easyocr_validation(table_roi, doc_key, len(rows))

        used_gemini = False
        if not is_valid or conf < CONFIG["easyocr_conf_threshold"]:
            used_gemini = True
            print(f"   ⚠️ OCR Check Failed (Valid: {is_valid}, Conf: {conf:.2f}). Triggering Gemini...")
            gemini_data = call_gemini_fallback(doc_key, crop_path, raw_ocr_blocks, rows)
            if gemini_data:
                extracted_data = gemini_data

        final_doc_results = {}
        for _, template_id, row_num in rows:
            votes = extracted_data.get(str(row_num), {}).get("numeric", "0")
            cleaned_vote = clean_digit(votes)
            results_map[template_id] = cleaned_vote
            final_doc_results[template_id] = cleaned_vote

        checkpoint_log[doc_key] = {
            "used_gemini": used_gemini,
            "final_results": final_doc_results
        }
        with open(CONFIG["json_log_path"], "w", encoding="utf-8") as f:
            json.dump(checkpoint_log, f, ensure_ascii=False, indent=4)

        for vr in val_rows:
            vr["Gemini Triggered"] = used_gemini
            vr["Final Output Vote"] = final_doc_results.get(list(final_doc_results.keys())[int(vr["Row"])-1], "0") if int(vr["Row"]) <= len(final_doc_results) else "0"
            global_val_list.append(vr)

        pd.DataFrame(global_val_list).to_csv(CONFIG["validation_csv"], index=False, encoding='utf-8-sig')

        total_rows_processed += len(rows)
        if total_rows_processed - last_notified_rows >= 50:
            print(f"\n📊 --- Progress: ดำเนินการเสร็จสิ้นแล้วทั้งหมด {total_rows_processed} แถว ---")
            last_notified_rows = total_rows_processed

        # ล้าง GPU VRAM หลังจากจบแต่ละภาพ ป้องกัน OOM (Out of Memory)
        torch.cuda.empty_cache()

    try:
        df_template = pd.read_csv(CONFIG["csv_path"], encoding='utf-8-sig')
        df_template['votes'] = df_template['id'].map(results_map).fillna("0")
        df_template[['id', 'votes']].to_csv(CONFIG["output_csv"], index=False)
    except pd.errors.EmptyDataError:
        print("❌ Error: ไฟล์ CSV template ว่างเปล่า")

    shutil.make_archive('/content/cropped_images', 'zip', CONFIG["crop_dir"])
    print("\n🎉 DONE. Checkpoint, JSON, Validation Table, and Cropped Images are saved.")

if __name__ == "__main__":
    df_tmp = pd.read_csv(CONFIG["csv_path"], encoding='utf-8-sig')
    doc_groups = {}
    for idx, row in df_tmp.iterrows():
        p = parse_id(row['id'])
        dk = p.get('doc_key')
        if dk:
            if dk not in doc_groups: doc_groups[dk] = []
            doc_groups[dk].append((idx, row['id'], p['row']))

    image_map = {}
    for dk in doc_groups:
        files_found = []
        for f in os.listdir(CONFIG["img_dir"]):
            f_dk, f_page = get_doc_key_and_page(f)
            if f_dk == dk: files_found.append(os.path.join(CONFIG["img_dir"], f))
        image_map[dk] = sorted(files_found, key=lambda x: get_doc_key_and_page(os.path.basename(x))[1])

    process_pipeline(doc_groups, image_map)

    for f in [CONFIG["output_csv"], CONFIG["validation_csv"], CONFIG["json_log_path"], '/content/cropped_images.zip']:
        if os.path.exists(f): files.download(f)

