def extract_table_grid(image_paths, doc_key):
    images = []
    for path in image_paths:
        img = cv2.imread(path)
        if img is None: continue

        gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
        h_img, w_img = gray.shape

        corner_mean = np.mean([
            np.mean(gray[0:10, 0:10]), np.mean(gray[0:10, -10:]),
            np.mean(gray[-10:, 0:10]), np.mean(gray[-10:, -10:])
        ])
        if corner_mean < 100:
            _, thresh = cv2.threshold(gray, 127, 255, cv2.THRESH_BINARY)
            contours, _ = cv2.findContours(thresh, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
            if contours:
                c = max(contours, key=cv2.contourArea)
                if cv2.contourArea(c) > (h_img * w_img * 0.2):
                    x, y, w_c, h_c = cv2.boundingRect(c)
                    img = img[y:y+h_c, x:x+w_c]
            # 🚀 FIX 2: ลบ cv2.bitwise_not ออก ป้องกันภาพกลายเป็นสีดำตัวหนังสือขาวจน OCR หลอน

        images.append(img)

    if not images: return None

    max_w = max(img.shape[1] for img in images)
    padded_images = [cv2.copyMakeBorder(img, 0, 0, 0, max_w - img.shape[1], cv2.BORDER_CONSTANT, value=[255, 255, 255]) for img in images]
    merged_img = cv2.vconcat(padded_images)

    # ดักจับภาพใหญ่เกินขีดจำกัด SHRT_MAX ป้องกัน OpenCV พัง
    MAX_DIM = 32000
    if merged_img.shape[0] > MAX_DIM or merged_img.shape[1] > MAX_DIM:
        scale = MAX_DIM / max(merged_img.shape[0], merged_img.shape[1])
        merged_img = cv2.resize(merged_img, (int(merged_img.shape[1] * scale), int(merged_img.shape[0] * scale)), interpolation=cv2.INTER_AREA)

    gray_merged = cv2.cvtColor(merged_img, cv2.COLOR_BGR2GRAY)

    gray_merged = cv2.normalize(gray_merged, None, 0, 255, cv2.NORM_MINMAX)
    clahe = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(8,8))
    enhanced_img = clahe.apply(gray_merged)

    final_img = cv2.cvtColor(enhanced_img, cv2.COLOR_GRAY2BGR)

    del images, padded_images, merged_img, gray_merged, enhanced_img
    gc.collect()

    return final_img

