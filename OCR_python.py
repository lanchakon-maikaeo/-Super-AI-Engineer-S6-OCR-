def run_easyocr_validation(table_roi, doc_key, expected_row_count):
    if table_roi is None: return {}, 0.0, False, [], []

    results = reader.readtext(table_roi, width_ths=1.5, x_ths=2.0)
    score_blocks = []
    anchor_y = 0
    stop_y = float('inf')
    anchor_found = False

    for bbox, text, conf in results:
        raw_text = text.strip()
        clean_text = raw_text.replace(" ", "")
        if "รวมคะแนนทั้งสิ้น" in clean_text or fuzz.partial_ratio("รวมคะแนนทั้งสิ้น", clean_text) >= 80:
             stop_y = min(stop_y, bbox[0][1])

    for bbox, text, conf in results:
        raw_text = text.strip()
        y_center = (bbox[0][1] + bbox[2][1]) / 2
        clean_text = raw_text.replace(" ", "")

        if "ได้คะแนน" in clean_text or fuzz.partial_ratio("ได้คะแนน", clean_text) >= 80:
            anchor_y = max(bbox[2][1], bbox[3][1])
            anchor_found = True
            continue

        if anchor_found and anchor_y < y_center < stop_y:
            match = re.search(r'([0-9๐-๙oOlISsBq|,\s]+)(?:[\(\[\{\|\u0e31\s]+)?([ก-๙]+)(?:[\)\]\}\|\u0e31\s]+)?', raw_text)
            if match:
                score_blocks.append({
                    "y": y_center,
                    "digit": clean_digit(match.group(1)),
                    "word": match.group(2).replace(" ", ""),
                    "conf": float(conf),
                    "raw_text": raw_text
                })

    score_blocks = sorted(score_blocks, key=lambda x: x['y'])

    extracted_data = {}
    conf_list = []
    is_valid = True
    val_rows = []

    if len(score_blocks) == expected_row_count:
        for i, block in enumerate(score_blocks):
            party_no = str(i + 1)
            digit_val = int(block['digit']) if block['digit'] else 0
            parsed_thai_num = thai_text_to_number(block['word'])

            status = "PASS"
            if parsed_thai_num != -1 and parsed_thai_num != digit_val:
                is_valid = False
                status = "MISMATCH"

            extracted_data[party_no] = {"numeric": block['digit']}
            conf_list.append(block['conf'])

            val_rows.append({
                "Doc Key": doc_key, "Row": party_no, "OCR Raw Text": block['raw_text'],
                "OCR Digit": block['digit'], "OCR Word": block['word'],
                "Parsed Thai Num": parsed_thai_num if parsed_thai_num != -1 else "อ่านไม่ออก",
                "OCR Conf": f"{block['conf']:.2f}", "Status": status
            })
        avg_conf = sum(conf_list) / len(conf_list) if conf_list else 0.0
    else:
        is_valid = False
        avg_conf = sum([b['conf'] for b in score_blocks]) / len(score_blocks) if score_blocks else 0.0

    return extracted_data, avg_conf, is_valid, score_blocks, val_rows

# 🎯 ระบบสลับ Model 2.5 Flash -> 2.5 Pro
def call_gemini_fallback(doc_key, crop_img_path, ocr_json_data, expected_rows_data):
    print(f"   🤖 Gemini Fallback: {doc_key} (แก้ไขข้อผิดพลาดของ OCR)")
    expected_keys = [str(r[2]) for r in expected_rows_data]

    prompt = f"""
    You are an expert OCR validator for Thai election tally sheets.
    Your task is to extract the FINAL VOTE COUNT for each party row.
    Expected row numbers: {", ".join(expected_keys)}.

    Context from EasyOCR (may contain errors or missing rows):
    {json.dumps(ocr_json_data, ensure_ascii=False)}

    INSTRUCTIONS:
    1. Read the attached image carefully. Look at the column that contains the vote counts.
    2. Convert Thai text numbers into Arabic numerals to cross-check.
    3. Return ONLY a valid JSON object matching this exact schema:
    {{"data": {{"1": {{"numeric": "10"}}, "2": {{"numeric": "0"}}, ... }}}}
    """

    img_content = Image.open(crop_img_path)

    try:
        # 🎯 ลองเรียกใช้ Gemini 3.0 Flash ก่อนเสมอ
        resp = client.models.generate_content(
            model="gemini-3-flash-preview",
            contents=[prompt, img_content],
            config={"temperature": 0.0, "response_mime_type": "application/json"}
        )
        text_resp = resp.text.replace("```json", "").replace("```", "").strip()
        return json.loads(text_resp).get("data", {})

    except Exception as e:
        print(f"   ⚠️ Gemini 3.0 Flash Error: {e}")
        print("   🔄 Switching to Gemini 2.5 flash..")
        try:
            # 🎯 ถ้า Flash เกิด Error จะสลับมาใช้ Gemini 2.5 flash ในรอบนี้
            resp_pro = client.models.generate_content(
                model="gemini-2.5-flash",
                contents=[prompt, img_content],
                config={"temperature": 0.0, "response_mime_type": "application/json"}
            )
            text_resp_pro = resp_pro.text.replace("```json", "").replace("```", "").strip()
            return json.loads(text_resp_pro).get("data", {})

        except Exception as e_pro:
            print(f"   ❌ Gemini 2.5 flash also Failed: {e_pro}")
            return {}

