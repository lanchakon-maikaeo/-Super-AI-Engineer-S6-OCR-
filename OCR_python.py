def clean_digit(raw):
    raw = str(raw).replace(',', '').replace(' ', '')
    raw = raw.replace('l', '1').replace('I', '1').replace('|', '1')
    raw = raw.replace('O', '0').replace('D', '0').replace('o', '0')
    # 🚀 FIX 1: เพิ่ม Confusion Matrix ดักจับตัวอักษรที่ OCR มักอ่านพลาดเป็นเลข
    raw = raw.replace('S', '5').replace('s', '5').replace('B', '8')
    raw = raw.replace('Z', '2').replace('z', '2').replace('q', '9')

    mapping_table = str.maketrans('๐๑๒๓๔๕๖๗๘๙o', '01234567890')
    cleaned = raw.translate(mapping_table)
    cleaned = re.sub(r'\D', '', cleaned)
    return cleaned if cleaned else "0"

def thai_text_to_number(text):
    if not text: return -1
    text = text.replace(" ", "").replace("ทมื่น", "หมื่น").replace("สิบย", "สิบ")

    num_map = {'ศูนย์':0, 'หนึ่ง':1, 'หนึง':1, 'เอ็ด':1, 'สอง':2, 'ยี่':2, 'สาม':3, 'สี่':4, 'ห้า':5, 'หก':6, 'เจ็ด':7, 'แปด':8, 'เก้า':9}
    pos_map = {'สิบ':10, 'ร้อย':100, 'พัน':1000, 'หมื่น':10000, 'แสน':100000, 'ล้าน':1000000}

    pattern = r'(ศูนย์|หนึ่ง|หนึง|เอ็ด|สอง|ยี่|สาม|สี่|ห้า|หก|เจ็ด|แปด|เก้า|สิบ|ร้อย|พัน|หมื่น|แสน|ล้าน)'
    tokens = re.findall(pattern, text)
    if not tokens: return -1

    total = 0
    current_val = 0
    for token in tokens:
        if token in num_map:
            current_val = num_map[token]
        elif token in pos_map:
            if current_val == 0 and token == 'สิบ':
                current_val = 1
            total += current_val * pos_map[token]
            current_val = 0

    total += current_val
    return total

